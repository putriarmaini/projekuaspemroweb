<div class="container mt-5">
    <p>Air bersih dan sanitasi layak adalah kebutuhan dasar manusia. Salah satu poin dalam tujuan pembangunan berkelanjutan (sustainable development goals/SDGs) pada sektor lingkungan hidup adalah memastikan masyarakat mencapai akses universal air bersih dan sanitasi.</p>
    <br>
    <p>Sekjen PBB menetapkan 27 Panel Tingkat Tinggi pada bulan Juli 2012. Panel Tingkat Tinggi merupakan kemitraan global yang bertujuan untuk memberantas kemiskinan dan mengubah perekonomian melalui pembangunan berkelanjutan.</p>
    <br>
    <p>Fokus utama ada pada ketersediaan pangan, air bersih, dan energi yang merupakan dasar dari kehidupan. Perubahan yang paling penting dalam konsumsi berkelanjutan dan produksi akan didorong oleh teknologi, inovasi, desain produk , pedoman kebijakan yang terperinci, pendidikan, dan perubahan perilaku. Panel mengusulkan dua belas Universal Goals dan Nasional Target. Target tersebut menyerukan pada negara-negara untuk “Mencapai universal akses dalam sektor air minum dan sanitasi” yang diharapkan dapat tercapai pada tahun 2030.</p>
    <br>
    <p>Bank Dunia pada 2014 mengingatkan 780 juta orang tidak memiliki akses air bersih dan lebih dari 2 miliar penduduk bumi tidak memiliki akses terhadap sanitasi. Akibatnya ribuan nyawa melayang tiap hari dan kerugian materi hingga 7 persen dari PDB dunia.</p>
    <br>
    <p>Sanitasi, begitu juga air bersih, secara khusus dibahas pada tujuan enam SDGs, walaupun tetap perlu menjadi catatan bahwa tujuan-tujuan yang ada ini sesungguhnya merupakan suatu kesatuan.</p>
